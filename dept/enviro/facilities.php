          <h6 style = color:blue;"><b>CENTRE OF MINING ENVIRONMENT (CME): A Centre of Excellence of MOEF/ GOI</b></h6>
<br>
          <h6 style = color:blue;"><b><u>About the Center</u></b></h6><p>

          A unique Centre of its kind ever since its establishment in 1987 under the sponsorship of Ministry of Environment & Forests, Govt. of India, the Centre has been carrying out advanced research in Environmental Science and Engineering with special emphasis on Mining Environment.<br>

          The Centre has been conducting various types of Executive Development Programs since its inception. The Centre has successfully completed two mega World Bank assisted Projects on Environmental Capacity Building for Coal India Limited and also build the capacity of Non- coal Mining Sector under World Bank assisted project of Ministry of Environment and Forests /GOI.<br>

          The Centre has developed various modular Executive Development Programs on Environmental Management in Mining Areas, Environmental Impact Assessment, Water Management, Land Management, Air and Noise Pollution Management, Noise and Human Response, Socio-economics and Mitigation and Social Impacts and Environmental Legislation and its Implications, EIA & Auditing.<br>

          The Centre has organised over 100 training programmes with the training of over 1000 in-service personnel and other stakeholders and offers regular training of 1-2-4-6-13 weeks duration as per the requirements of the industry. The training program of 13 weeks duration has been recognized as fulfillment of requirement for environmental cadre of its executives by CIL. The Centre is currently offering programmes as per the requirement of the industry. It also offers consultancy services to the industry in the areas of EIA, environmental management, environmental auditing, air and water pollution control, wastewater management, reclamation of degraded lands, solid waste management, noise abatement, etc.<br>

          The Centre has resulted Manuals, Books and Compilations, which serves the mineral industry as reference material. The Centre’s Faculty won many laurels which include National Mineral Award, Bala Tandan and M.L.Rungta Awards (MGMI & MEAI), Institution of Engineer(s) Gold Medal, Subarna and Sashibhusan Memorial Award, Bharat Jyoti Award, Michael Madhusudan Award, etc. The Centre assisted the regulatory agencies (CPCB/MOEF) in developing environmental standards for coal mining areas, guidelines for dust suppressant chemicals, mining in the forest land and also preparation of operation Damodar Clean Action Plan. The Centre faculty served various high level committees of Government of India including MOEF/GOI, BIS, DGMS, MOC etc. Professor Gurdeep Singh is currently the Member Environmental Expert Appraisal Committee (Mining) of MOEF/GOI.</p><br>

          <h6 style = color:blue;"><b><u>ENVIS CENTRE</u></b></h6><br>

          The Environmental Information System (ENVIS) at Centre of Mining Environment (CME), Indian School of Mines (ISM), was established in 1991 by the Ministry of Environment and Forests (MoEF), Government of India, for collection, storage, retrieval and dissemination of information in the area of mining environment.<br>

          Our ENVIS Centre is accessible online since September 14, 2001 with a distinctive website “http://www.geocities.com/envis_ism” which was launched by Shri P.V.Jaykrishnan(IAS), Secretary to Government of India, MoEF. This website has also been provided on NIC platform with a distinctive URL “http://www.ismenvis.nic.in”.<br>

          The website has links to different organizations viz., different ENVIS Centres, Coal & non-coal Organizations, Central Govt(Ministries) & Apex/Independent organizations, Mineral rich States, Scientific Institutions, Universities, Organizations related to Environment and Mining. Centre has provided variety of information in the website viz., subject area, Best Practices Management, environmental clearance related to mining, legislation, publications, database, experts, workshop report, important Institutes, environmental awareness, research papers, research projects, current events, current news, do you know section, equipments, photo gallery, special lectures including Prof. S.K.Bose Memorial Lectures, query form, feedback form, etc.<br>
         
          Centre also collected many Reports, Research articles, Proceedings and Newsletters from various sources. All these hard documents are kept in the ENVIS library for ready references.<br>
         
          Centre results publications on regular basis and till date 54 Newsletters, 14 Monographs, 9 Books have been published and 7 Compilations of Research Publications of CME have also been made.<br><br><br>

