              <p id="arvcenter08"><strong>TESTING SERVICES</strong></p>
              <p>Testing of coal and rocks, mine supports, construction materials, mine gases, etc., is done on a regular basis in the laboratories of the Department.</p>
              <ul>
                <li>The laboratories have facilities for determination of almost all the properties of rock that are required for planning, design and operation of mines. These facilities are used extensively by the industry.</li>
