             <h4 id="arvcenter08"><strong>Main Electronics Lab</strong></h4>
             <p>This is the main lab where basic experiments like device characteristics and basic analog circuits are held. This lab has a number of standard setups, each setup consisting of advanced trainer kits, oscilloscopes, function generators and power supplies. This lab is general enough to hold a number of experiments suitable for giving students enough exposure to enable them to easily adopt experiments in more advanced laboratories in the department.</p>

             <h4 id="arvcenter08"><strong>Digital Electronics Lab</strong></h4>
             <p>This lab is used to conduct experiments in the domain of digital electronics. This lab is self sufficient in all respects. Each standard setup in the lab consists of basic and advanced trainer kits, oscilloscope and function generators.</p>

                <h4 id="arvcenter08"><strong>Communication Lab</strong></h4>
             <p>All analog and digital communication related experiments are conducted here. A standard setup in the lab consists of equipments like synthesized function generators, analog oscilloscopes, digital storage oscilloscopes, multiple power supplies and a Pentium-4 PC. This lab also has a number of advanced instruments like 100 Mhz 18 channel mixed signal oscilloscope and programmable function generator, LCR data bridges, various modulators and demodulators, filters, multiplexers and demultiplexers, communication trainer kits, spectrum analyzers, DSP boards, TIMS hardware and software module with evaluation module etc.</p>

             <h4 id="arvcenter08"><strong>Control Engineering Lab</strong></h4>
             <p>This laboratory is well equipped to handle experiments on control systems and embedded systems. This state –of-the-are laboratory has tools like Matlab with target embedded module with SimuLink, Real-time workshop and toolboxes like control system, signal processing, communication, image processing and neural networks. For practical experience on implementation there are LABView with six hardware setups, distributed control system setup with little genie software and ADAm’s hardware module and Silicon Lab PSOCs based on 8051 core with four user Keil compilers.</p>

             <h4 id="arvcenter08"><strong>Digital Signal Processing Lab</strong></h4>
             <p>This is a specialized laboratory with emphasis on signal processing experiments. Here students perform signal processing tasks in both hardware and softwere. While Matbal and SimuLink (with a set of took boxes) are used for simulation task, real-time experiments are carried out using DSP kits from TEXAS Instruments. There hardware boards, at present, available in the lab are TMS320C31, TMS320C6711 and TMS320CVC33 DSKs. Moreover, we have one TMS320C6701 EVM to experiment with the full capability of the processor. One color image acquisition system to take images of objects for further processing with software like Matlab, is also available. Beside these, arbitrary waveform generators, Digital Storage Oscilloscopes, scanner and color deskjet printers are also available in the lab.</p>


             <h4 id="arvcenter08"><strong>Fiber Optics Lab</strong></h4>
             <p>This is a highly specialized lab with many instruments not found ordinarily in educational institutes. This lab is well equipped to handle R&D level experiments in the domain of optical communication and fiber optics. This lab has optical spectrum analyzer, lock –in – amplifier, splice machine, DFB Laser diodes, 980 nm Tunable Laser Diode with accessories, 1500 to 1600 nm Tunable laser diode, OTDR for 850, 1300 & 1550 nm plug-in units & OTDR Emulation Software LEDs, LD & PDs of different wavelengths different types of connectors, adapters, pigtails, Lenses, polarizers, different types of SM, MM & plastic fibers as well as EDFA and many more such equipments are also available.</p>


             <h4 id="arvcenter08"><strong>Microprocessor Lab</strong></h4>
             <p>This lab has been established in the department to provide intensive practical exposure to the students in the field of microprocessor architecture and industrial control through microprocessors. This lab has a large number of 8085 and 8086 microprocessor kits, 8051/31 microcontroller trainer kits with assembler/compiler and development system facilities and various add-on cards like A/D converters, stepper motor controller kits, interfacing cards and simulator cards.</p>

             <h4 id="arvcenter08"><strong>Microwave Lab</strong></h4>
             <p>This is a specialized lab dealing with the study of microwave and antenna design, characteristics and properties. This lab consists of a number of setups each equipped with Klystron tubes, Gunn diodes, Klystron power supplies, Modulators, directional couplers, various power energy and VSWR meters etc. This lab also has antenna system trainers complete with 40 to 850 MHz receiver/transmitter, antennas and analysis software.This lab also has a Satellite communication system trainer kit to impart advanced skills to its students. This lab also has IE3D Simulation software for microwave circuit simulation and analysis.</p>

             <h4 id="arvcenter08"><strong>Nano-electronics and Analog Circuits Lab</strong></h4>
             <p>This is primarily a project lab sponsored by Ministry of Human Resource and Development (MHRD), Govt. Of Indian. It consists of 2 sections, one dealing with Nano-scale device modeling project and another with analog circuits. This lab has a number of P4 PCs equipped with Matlab and SimuLink,, MultiSim with analog and digital and mixed mode circuits and also PCB layout design. Facilities of hardware implementation of simulated analog circuit are also available in the lab. We are also in the process of procuring Medici Device Simulation software for advanced level device modeling and simulation.</p>

             <h4 id="arvcenter08"><strong>Communication Lab</strong></h4>
