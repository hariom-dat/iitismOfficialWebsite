      <h6><strong>1. Power Hydraulics Laboratory :</strong></h6><br><br>

      <strong> a. Computer controlled Closed Circuit and Open circuit Hydrostatic Transmission System of 15 kW capacity</strong><br>

It consists of open-loop and closed-loop hydraulic systems, where the load on the hydrostatic motor is controlled through pump loading.<br>
Open circuit system consists of pressure compensated axial piston pump with raidia piston motor, whereas closed circuit system consists of swash plate controlled variable displacement pump and low speed high torque radial piston motor<br>
In both the systems, the performance of the hydrostatic transmission systems in open and closed configurations can be tested by:<br>
• Controlling the load on the hydrostatic motor through pump loading<br>
• Controlling the speed of the hydrostatic motor by varying flow supplied by the main pump either by changing the swash plate angle or by varying its speed<br>
• Controlling the speed of the motor through flow control valve<br>
• Inertia load<br><br>


 

<strong>b. Computer controlled Laboratory Model Hydraulic Excavator</strong><br>

A small in-house fabricated digger / backhoe is operated through hydraulic systems. The movement of the front attachment of the hydraulic back-hoe is accomplished through the flow supplied from proportional valves that are controlled by the in-house developed PLC.<br>
The following experiments / studies can be performed:<br>
• Study of the kinematics of hydraulic backhoe<br>
• Control of the actuators of the backhoe through proportional valves that are operated through PLC<br>
• Interfacing hydraulic system with computer through AD/DA cards.<br><br>

 

<strong>c. controlled Proportional valve controlled hydraulic motor test set-up of 7.5 kW capacity</strong><br>

The system consists of two valve controlled open circuit hydrostatic drives; where either bent-axis motor or Gerotor motor can be controlled either through proportional valve or servo-proportional valve. In both cases, the load on the motor shaft is controlled through pump loading.<br>
In both the systems, the performance of the hydrostatic drives can be evaluated in open and closed circuit conditions by:<br>
• Controlling the load on the hydrostatic motor through pump loading<br>
• Controlling the speed of the hydrostatic motor by varying flow through proportional valves<br>
The system can also be used for determining the pressure-flow characteristics of the proportional and the servo-proportional valves.<br><br>



<strong>d. system with Accumulator</strong><br>

The system consists of 7.5 kW power pack with two 10 liter and 20 litre capacities of Accumulators. It supplies hydraulic power to a bent axis hydrostatic motor which is loaded through pump loading.<br>
The following experiments can be performed:<br>
• Energy saving of the system using different capacities of accumulators<br>
• Energy saving of the system at varying load<br>
• Energy saving of the system at different pre-charge pressure of the accumulators<br>
<br>
 

 

<strong>e. Programmable Logic Controlled Two-Motor Hydrostatic Summation Drive</strong><br>

It is a closed loop hydrostatic transmission system of 15 kW capacity. The system consists of a swashplate controlled variable displacement pump and two bent axis motors. Either one or two motors can be connected hydraulically with the pump in closed loop configuration through PLC. Depending on the load controlled through PLC, the closed loop system can be operated either with one or two motors The following experiments can be performed:<br>
• Performance of the closed loop hydrostatic transmission system with single motor drive at varying load achieved through pump loading. The pump flow is varied by changing its swashplate angle through PLC.<br>
• Performance of the closed loop hydrostatic transmission system with two motor drive at varying load achieved through pump loading. The pump flow is varied by changing its swashplate angle through PLC.<br>
• Automatic changing from single motor to two motor drive or vice-versa with the changing of motor loading thorugh PLC<br>
<br>
 

 

<strong>f. Hydrostatic transmission system with pump loading</strong><br>

This is sponsored by DST, New Delhi, under fast tract SERC project (project value: Rs. 17.4 Lakh) during the tenure 2009-2012. The setup comprises of a hydraulic power pack, a hydraulic motor with a control valve, a pump with loading circuit and a PLC (Programmable Logic Controller) operated Control Panel. The load on the system can be varied by changing set-pressure of a Pressure Relief Valve (PRV). The fluid pressures at the inlet and outlet side of the hydraulic motor and outlet side of the pump are measured by pressure transducers and data are transmitted to PC through Data Acquisition System (DAS) mounted inside the control panel for the purpose of continuous monitoring, performance analysis and online fault diagnosis.<br><br>
